    public function store(Request $request)
    {
        $validation = Validator::make($request->all());

        if($validation->fails()){
            return $validation->errors()->toJson();
        }

        $student = new Student();
        $student->fill($request->all());
        $student->save();

        return response()->json($student, 201);
    }

